// --- A recording 2d context: fillRect ops land in `ops` with the live translate. ---
let ops = [];
function recordingCtx() {
  const st = { tx: 0, ty: 0, stack: [], fillStyle: '#000' };
  return {
    get canvas() { return { width: 304, height: 208 }; },
    set fillStyle(v) { st.fillStyle = v; },
    get fillStyle() { return st.fillStyle; },
    fillRect(x, y, w, h) { ops.push([x + st.tx, y + st.ty, w, h, st.fillStyle]); },
    save() { st.stack.push([st.tx, st.ty]); },
    restore() { const p = st.stack.pop(); if (p) { st.tx = p[0]; st.ty = p[1]; } },
    translate(x, y) { st.tx += x; st.ty += y; },
    drawImage() {}, // keeper sprites respawn as live villagers in Rust
    beginPath() {}, ellipse() {}, arc() {}, fill() {}, stroke() {}, clip() {}, rect() {},
    strokeRect() {}, moveTo() {}, lineTo() {}, closePath() {},
    set strokeStyle(_) {}, set globalAlpha(_) {}, set lineWidth(_) {},
  };
}

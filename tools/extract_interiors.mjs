// Colors: '#rgb'/'#rrggbb'/'rgba(r,g,b,a)' -> 0xRRGGBBAA.
function rgba(col) {
  if (col.startsWith('#')) {
    let h = col.slice(1);
    if (h.length === 3) h = h.split('').map((c) => c + c).join('');
    return (parseInt(h, 16) * 256 + 255) >>> 0;
  }
  const m = col.match(/rgba?\(([\d.]+)[, ]+([\d.]+)[, ]+([\d.]+)(?:[, ]+([\d.]+))?\)/);
  if (!m) throw new Error('color? ' + col);
  const a = m[4] == null ? 255 : Math.round(parseFloat(m[4]) * 255);
  return (((+m[1] << 24) | (+m[2] << 16) | (+m[3] << 8) | a) >>> 0);
}

